                $type->setSourceReference(new FHIRReference($json[self::FIELD_SOURCE_REFERENCE]));
            }
        }
        return $type;
    }

    /**
     * @return \stdClass
     */
    public function jsonSerialize(): mixed
    {
        $out = parent::jsonSerialize();
        if (isset($this->language)) {
            $out->language = $this->language;
        }
        if (isset($this->sourceString)) {
            if (null !== ($val = $this->sourceString->getValue())) {
                $out->sourceString = $val;
            }
            $ext = $this->sourceString->jsonSerialize();
            unset($ext->value);
            if (count((array)$ext) > 0) {
                $out->_sourceString = $ext;
            }
        }
        if (isset($this->sourceReference)) {
            $out->sourceReference = $this->sourceReference;
        }
        return $out;
    }

    /**
     * @return string
     */
    public function __toString(): string
    {
        return self::FHIR_TYPE_NAME;
    }
