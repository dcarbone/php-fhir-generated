                || $this->rater->_nonValueFieldDefined())) {
            $xw->startElement(self::FIELD_RATER);
            $this->rater->xmlSerialize($xw, $config, $this->_valueXMLLocations[self::FIELD_RATER]);
            $xw->endElement();
        }
        if (isset($this->subcomponent)) {
            foreach ($this->subcomponent as $v) {
                $xw->startElement(self::FIELD_SUBCOMPONENT);
                $v->xmlSerialize($xw, $config);
                $xw->endElement();
            }
        }
    }

