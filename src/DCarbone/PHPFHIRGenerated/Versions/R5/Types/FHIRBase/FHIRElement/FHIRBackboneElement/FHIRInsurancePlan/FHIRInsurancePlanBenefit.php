        if (isset($json[self::FIELD_REQUIREMENT]) || isset($json[self::FIELD_REQUIREMENT_EXT]) || array_key_exists(self::FIELD_REQUIREMENT, $json) || array_key_exists(self::FIELD_REQUIREMENT_EXT, $json)) {
            $value = $json[self::FIELD_REQUIREMENT] ?? null;
            $ext = (array)($json[self::FIELD_REQUIREMENT_EXT] ?? []);
            $type->setRequirement(FHIRString::jsonUnserialize(
                json: [FHIRString::FIELD_VALUE => $value] + $ext,
                config: $config,
            ));
        }
        if (isset($json[self::FIELD_LIMIT]) || array_key_exists(self::FIELD_LIMIT, $json)) {
            $vs = $json[self::FIELD_LIMIT];
            if (!is_int(key($vs))) {
                $vs = [$vs];
            }
            foreach($vs as $v) {
                $type->addLimit(FHIRInsurancePlanLimit::jsonUnserialize(
                    json: $v,
                    config: $config,
                ));
            }
        }
        return $type;
    }

    /**
     * @return \stdClass
     */
    public function jsonSerialize(): mixed
    {
        $out = parent::jsonSerialize();
        if (isset($this->type)) {
            $out->type = $this->type;
        }
        if (isset($this->requirement)) {
            if (null !== ($val = $this->requirement->getValue())) {
                $out->requirement = $val;
            }
            $ext = $this->requirement->jsonSerialize();
            unset($ext->value);
            if (count((array)$ext) > 0) {
                $out->_requirement = $ext;
            }
        }
        if (isset($this->limit) && [] !== $this->limit) {
            $out->limit = $this->limit;
        }
        return $out;
    }

    /**
     * @return string
     */
    public function __toString(): string
    {
        return self::FHIR_TYPE_NAME;
    }
