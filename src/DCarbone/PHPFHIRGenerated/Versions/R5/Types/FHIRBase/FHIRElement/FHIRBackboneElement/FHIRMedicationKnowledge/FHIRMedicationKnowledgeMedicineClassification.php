                $vals = $decoded->classification;
            }
            foreach($vals as $v) {
                $type->addClassification(FHIRCodeableConcept::jsonUnserialize($v, $config));
            }
        }
        return $type;
    }

    /**
     * @return \stdClass
     */
    public function jsonSerialize(): mixed
    {
        $out = parent::jsonSerialize();
        if (isset($this->type)) {
            $out->type = $this->type;
        }
        if (isset($this->sourceString)) {
            if (null !== ($val = $this->sourceString->getValue())) {
                $out->sourceString = $val;
            }
            if ($this->sourceString->_nonValueFieldDefined()) {
                $ext = $this->sourceString->jsonSerialize();
                unset($ext->value);
                $out->_sourceString = $ext;
            }
        }
        if (isset($this->sourceUri)) {
            if (null !== ($val = $this->sourceUri->getValue())) {
                $out->sourceUri = $val;
            }
            if ($this->sourceUri->_nonValueFieldDefined()) {
                $ext = $this->sourceUri->jsonSerialize();
                unset($ext->value);
                $out->_sourceUri = $ext;
            }
        }
        if (isset($this->classification) && [] !== $this->classification) {
            if ($this->_getJSONFieldElideSingletonArray(self::FIELD_CLASSIFICATION) && 1 === count($this->classification)) {
                $out->classification = $this->classification[0];
            } else {
                $out->classification = $this->classification;
            }
        }
        return $out;
    }
