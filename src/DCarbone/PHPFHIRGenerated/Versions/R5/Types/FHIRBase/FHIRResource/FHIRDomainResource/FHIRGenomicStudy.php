            if ($this->instantiatesUri->_nonValueFieldDefined()) {
                $ext = $this->instantiatesUri->jsonSerialize();
                unset($ext->value);
                $out->_instantiatesUri = $ext;
            }
        }
        if (isset($this->note) && [] !== $this->note) {
            if ($this->_getJSONFieldElideSingletonArray(self::FIELD_NOTE) && 1 === count($this->note)) {
                $out->note = $this->note[0];
            } else {
                $out->note = $this->note;
            }
        }
        if (isset($this->description)) {
            if (null !== ($val = $this->description->getValue())) {
                $out->description = $val;
            }
            if ($this->description->_nonValueFieldDefined()) {
                $ext = $this->description->jsonSerialize();
                unset($ext->value);
                $out->_description = $ext;
            }
        }
        if (isset($this->analysis) && [] !== $this->analysis) {
            if ($this->_getJSONFieldElideSingletonArray(self::FIELD_ANALYSIS) && 1 === count($this->analysis)) {
                $out->analysis = $this->analysis[0];
            } else {
                $out->analysis = $this->analysis;
            }
        }
        $out->resourceType = $this->_getResourceType();
        return $out;
    }
