                json: [FHIRUrl::FIELD_VALUE => $value] + $ext,
                config: $config,
            ));
        }
        if (isset($json[self::FIELD_HEADER]) || isset($json[self::FIELD_HEADER_EXT]) || array_key_exists(self::FIELD_HEADER, $json) || array_key_exists(self::FIELD_HEADER_EXT, $json)) {
            $value = $json[self::FIELD_HEADER] ?? null;
            $ext = (array)($json[self::FIELD_HEADER_EXT] ?? []);
            if (!is_array($value)) {
                $value = [$value];
            }
            $cnt = count($value);
            $extCnt = count($ext);
            if ($extCnt > $cnt) {
                $cnt = $extCnt;
            }
            for ($i = 0; $i < $cnt; $i++) {
                $type->addHeader(FHIRString::jsonUnserialize(
                    json: [FHIRString::FIELD_VALUE => $value[$i] ?? null] + (array)($ext[$i] ?? []),
                    config: $config,
                ));
            }
        }
        return $type;
    }

    /**
     * @return \stdClass
     */
    public function jsonSerialize(): mixed
    {
        $out = parent::jsonSerialize();
        if (isset($this->identifier) && [] !== $this->identifier) {
            $out->identifier = $this->identifier;
        }
        if (isset($this->status)) {
            if (null !== ($val = $this->status->getValue())) {
                $out->status = $val;
            }
            $ext = $this->status->jsonSerialize();
            unset($ext->value);
            if (count((array)$ext) > 0) {
                $out->_status = $ext;
            }
        }
        if (isset($this->connectionType) && [] !== $this->connectionType) {
            $out->connectionType = $this->connectionType;
        }
        if (isset($this->name)) {
            if (null !== ($val = $this->name->getValue())) {
                $out->name = $val;
            }
            $ext = $this->name->jsonSerialize();
            unset($ext->value);
            if (count((array)$ext) > 0) {
                $out->_name = $ext;
            }
        }
        if (isset($this->description)) {
            if (null !== ($val = $this->description->getValue())) {
                $out->description = $val;
            }
            $ext = $this->description->jsonSerialize();
            unset($ext->value);
            if (count((array)$ext) > 0) {
                $out->_description = $ext;
            }
        }
        if (isset($this->environmentType) && [] !== $this->environmentType) {
            $out->environmentType = $this->environmentType;
        }
        if (isset($this->managingOrganization)) {
            $out->managingOrganization = $this->managingOrganization;
        }
        if (isset($this->contact) && [] !== $this->contact) {
            $out->contact = $this->contact;
        }
        if (isset($this->period)) {
            $out->period = $this->period;
        }
        if (isset($this->payload) && [] !== $this->payload) {
            $out->payload = $this->payload;
        }
        if (isset($this->address)) {
            if (null !== ($val = $this->address->getValue())) {
                $out->address = $val;
            }
            $ext = $this->address->jsonSerialize();
            unset($ext->value);
            if (count((array)$ext) > 0) {
                $out->_address = $ext;
            }
        }
        if (isset($this->header) && [] !== $this->header) {
            $vals = [];
            $exts = [];
            foreach ($this->header as $v) {
                $val = $v->getValue();
                $ext = $v->jsonSerialize();
                unset($ext->value);
                if (null !== $val) {
                    $vals[] = $val;
                }
                if ([] !== $ext) {
                    $exts[] = $ext;
                }
            }
            if ([] !== $vals) {
                $out->header = $vals;
            }
            if (count((array)$ext) > 0) {
                $out->_header = $exts;
            }
        }
        $out->resourceType = $this->_getResourceType();
        return $out;
    }

