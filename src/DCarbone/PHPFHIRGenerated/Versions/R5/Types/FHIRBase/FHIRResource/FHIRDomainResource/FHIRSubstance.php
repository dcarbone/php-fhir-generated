        }
        if (!$xw->isDocStarted()) {
            $docStarted = true;
            $xw->startDocument();
        }
        if (!$xw->isRootOpen()) {
            $rootOpened = true;
            $xw->openRootNode('Substance', $this->_getSourceXMLNS());
        }
        if (isset($this->instance) && $this->instance->_getValueXMLLocation() === ValueXMLLocationEnum::ATTRIBUTE) {
