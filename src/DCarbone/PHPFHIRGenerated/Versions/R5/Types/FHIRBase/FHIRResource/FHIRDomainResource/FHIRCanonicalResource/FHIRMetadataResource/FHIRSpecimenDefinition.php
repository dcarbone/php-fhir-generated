        if (isset($this->approvalDate)) {
            if (null !== ($val = $this->approvalDate->getValue())) {
                $out->approvalDate = $val;
            }
            $ext = $this->approvalDate->jsonSerialize();
            unset($ext->value);
            if (count((array)$ext) > 0) {
                $out->_approvalDate = $ext;
            }
        }
        if (isset($this->lastReviewDate)) {
            if (null !== ($val = $this->lastReviewDate->getValue())) {
                $out->lastReviewDate = $val;
            }
            $ext = $this->lastReviewDate->jsonSerialize();
            unset($ext->value);
            if (count((array)$ext) > 0) {
                $out->_lastReviewDate = $ext;
            }
        }
        if (isset($this->effectivePeriod)) {
            $out->effectivePeriod = $this->effectivePeriod;
        }
        if (isset($this->typeCollected)) {
            $out->typeCollected = $this->typeCollected;
        }
        if (isset($this->patientPreparation) && [] !== $this->patientPreparation) {
            $out->patientPreparation = $this->patientPreparation;
        }
        if (isset($this->timeAspect)) {
            if (null !== ($val = $this->timeAspect->getValue())) {
                $out->timeAspect = $val;
            }
            $ext = $this->timeAspect->jsonSerialize();
            unset($ext->value);
            if (count((array)$ext) > 0) {
                $out->_timeAspect = $ext;
            }
        }
        if (isset($this->collection) && [] !== $this->collection) {
            $out->collection = $this->collection;
        }
        if (isset($this->typeTested) && [] !== $this->typeTested) {
            $out->typeTested = $this->typeTested;
        }
        $out->resourceType = $this->_getResourceType();
        return $out;
    }

