                ltrim(substr(__CLASS__, (int)strrpos(__CLASS__, '\\')), '\\'),
                static::class,
                get_class($type)
            ));
        }
        if (null === $config) {
            $config = (new Version())->getConfig()->getUnserializeConfig();
        }
        if (is_string($json)) {
            $json = json_decode(json: $json,
                                associative: false,
                                depth: $config->getJSONDecodeMaxDepth(),
                                flags: $config->getJSONDecodeOpts());
        }
        parent::jsonUnserialize($json, $config, $type); 
        if (isset($json->identifier) || property_exists($json, self::FIELD_IDENTIFIER)) {
            if (is_object($json->identifier)) {
                $vals = [$json->identifier];
                $type->_setJSONFieldElideSingletonArray(self::FIELD_IDENTIFIER, true);
            } else {
                $vals = $json->identifier;
            }
            foreach($vals as $v) {
                $type->addIdentifier(FHIRIdentifier::jsonUnserialize($v, $config));
            }
        }
        if (isset($json->subject) || property_exists($json, self::FIELD_SUBJECT)) {
            if (is_array($json->subject)) {
                $type->setSubject(FHIRResourceReference::jsonUnserialize(reset($json->subject), $config));
            } else {
                $type->setSubject(FHIRResourceReference::jsonUnserialize($json->subject, $config));
            }
        }
        if (isset($json->note)
            || isset($json->_note)
            || property_exists($json, self::FIELD_NOTE)
            || property_exists($json, self::FIELD_NOTE_EXT)) {
            $v = $json->_note ?? new \stdClass();
            $v->value = $json->note ?? null;
            $type->setNote(FHIRString::jsonUnserialize($v, $config));
        }
        if (isset($json->relation) || property_exists($json, self::FIELD_RELATION)) {
            if (is_object($json->relation)) {
                $vals = [$json->relation];
                $type->_setJSONFieldElideSingletonArray(self::FIELD_RELATION, true);
            } else {
                $vals = $json->relation;
            }
            foreach($vals as $v) {
                $type->addRelation(FHIRFamilyHistoryRelation::jsonUnserialize($v, $config));
            }
        }
        return $type;
    }

    /**
     * @return \stdClass
     */
    public function jsonSerialize(): mixed
    {
        $out = parent::jsonSerialize();
        if (isset($this->identifier) && [] !== $this->identifier) {
            if ($this->_getJSONFieldElideSingletonArray(self::FIELD_IDENTIFIER) && 1 === count($this->identifier)) {
                $out->identifier = $this->identifier[0];
            } else {
                $out->identifier = $this->identifier;
            }
        }
        if (isset($this->subject)) {
            $out->subject = $this->subject;
        }
        if (isset($this->note)) {
            if (null !== ($val = $this->note->getValue())) {
                $out->note = $val;
            }
            if ($this->note->_nonValueFieldDefined()) {
                $ext = $this->note->jsonSerialize();
                unset($ext->value);
                $out->_note = $ext;
            }
        }
        if (isset($this->relation) && [] !== $this->relation) {
            if ($this->_getJSONFieldElideSingletonArray(self::FIELD_RELATION) && 1 === count($this->relation)) {
                $out->relation = $this->relation[0];
            } else {
                $out->relation = $this->relation;
            }
        }
        $out->resourceType = $this->_getResourceType();
        return $out;
    }
