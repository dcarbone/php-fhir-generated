        return $type;
    }

    /**
     * @return \stdClass
     */
    public function jsonSerialize(): mixed
    {
        $out = parent::jsonSerialize();
        if (isset($this->flag) && [] !== $this->flag) {
            if ($this->_getJSONFieldElideSingletonArray(self::FIELD_FLAG) && 1 === count($this->flag)) {
                $out->flag = $this->flag[0];
            } else {
                $out->flag = $this->flag;
            }
        }
        if (isset($this->deleted)) {
            if (null !== ($val = $this->deleted->getValue())) {
                $out->deleted = $val;
            }
            if ($this->deleted->_nonValueFieldDefined()) {
                $ext = $this->deleted->jsonSerialize();
                unset($ext->value);
                $out->_deleted = $ext;
            }
        }
        if (isset($this->date)) {
            if (null !== ($val = $this->date->getValue())) {
                $out->date = $val;
            }
            if ($this->date->_nonValueFieldDefined()) {
                $ext = $this->date->jsonSerialize();
                unset($ext->value);
                $out->_date = $ext;
            }
        }
        if (isset($this->item)) {
            $out->item = $this->item;
        }
        return $out;
    }
