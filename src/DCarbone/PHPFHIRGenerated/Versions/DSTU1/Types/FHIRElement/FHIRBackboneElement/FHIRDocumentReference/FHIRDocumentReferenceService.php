                $out->address = $val;
            }
            if ($this->address->_nonValueFieldDefined()) {
                $ext = $this->address->jsonSerialize();
                unset($ext->value);
                $out->_address = $ext;
            }
        }
        if (isset($this->parameter) && [] !== $this->parameter) {
            $out->parameter = $this->parameter;
        }
        return $out;
    }
