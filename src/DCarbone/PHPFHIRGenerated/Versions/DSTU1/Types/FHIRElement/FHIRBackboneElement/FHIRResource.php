    {
        $out = parent::jsonSerialize();
        if (isset($this->language)) {
            if (null !== ($val = $this->language->getValue())) {
                $out->language = $val;
            }
            $ext = $this->language->jsonSerialize();
            unset($ext->value);
            if (count((array)$ext) > 0) {
                $out->_language = $ext;
            }
        }
        if (isset($this->text)) {
            $out->text = $this->text;
        }
        if (isset($this->contained) && [] !== $this->contained) {
            $out->contained = $this->contained;
        }
        return $out;
    }

