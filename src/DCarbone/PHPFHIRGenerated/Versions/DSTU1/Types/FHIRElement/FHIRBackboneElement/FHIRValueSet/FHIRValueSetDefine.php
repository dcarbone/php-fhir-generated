            || property_exists($json, self::FIELD_CASE_SENSITIVE_EXT)) {
            $v = $json->_caseSensitive ?? new \stdClass();
            $v->value = $json->caseSensitive ?? null;
            $type->setCaseSensitive(FHIRBoolean::jsonUnserialize($v, $config));
        }
        if (isset($json->concept) || property_exists($json, self::FIELD_CONCEPT)) {
            if (is_object($json->concept)) {
                $vals = [$json->concept];
                $type->_setJSONFieldElideSingletonArray(self::FIELD_CONCEPT, true);
            } else {
                $vals = $json->concept;
            }
            foreach($vals as $v) {
                $type->addConcept(FHIRValueSetConcept::jsonUnserialize($v, $config));
            }
        }
        return $type;
    }

    /**
     * @return \stdClass
     */
    public function jsonSerialize(): mixed
    {
        $out = parent::jsonSerialize();
        if (isset($this->system)) {
            if (null !== ($val = $this->system->getValue())) {
                $out->system = $val;
            }
            if ($this->system->_nonValueFieldDefined()) {
                $ext = $this->system->jsonSerialize();
                unset($ext->value);
                $out->_system = $ext;
            }
        }
        if (isset($this->version)) {
            if (null !== ($val = $this->version->getValue())) {
                $out->version = $val;
            }
            if ($this->version->_nonValueFieldDefined()) {
                $ext = $this->version->jsonSerialize();
                unset($ext->value);
                $out->_version = $ext;
            }
        }
        if (isset($this->caseSensitive)) {
            if (null !== ($val = $this->caseSensitive->getValue())) {
                $out->caseSensitive = $val;
            }
            if ($this->caseSensitive->_nonValueFieldDefined()) {
                $ext = $this->caseSensitive->jsonSerialize();
                unset($ext->value);
                $out->_caseSensitive = $ext;
            }
        }
        if (isset($this->concept) && [] !== $this->concept) {
            if ($this->_getJSONFieldElideSingletonArray(self::FIELD_CONCEPT) && 1 === count($this->concept)) {
                $out->concept = $this->concept[0];
            } else {
                $out->concept = $this->concept;
            }
        }
        return $out;
    }
