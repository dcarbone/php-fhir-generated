        if (!$xw->isDocStarted()) {
            $docStarted = true;
            $xw->startDocument();
        }
        if (!$xw->isRootOpen()) {
            $rootOpened = true;
            $xw->openRootNode('Supply', $this->_getSourceXMLNS());
        }
        if (isset($this->status) && ValueXMLLocationEnum::PARENT_ATTRIBUTE === $this->_valueXMLLocations[self::FIELD_STATUS]) {
            $xw->writeAttribute(self::FIELD_STATUS, $this->status->_getValueAsString());
        }
        parent::xmlSerialize($xw, $config);
        if (isset($this->kind)) {
            $xw->startElement(self::FIELD_KIND);
            $this->kind->xmlSerialize($xw, $config);
            $xw->endElement();
        }
        if (isset($this->identifier)) {
            $xw->startElement(self::FIELD_IDENTIFIER);
            $this->identifier->xmlSerialize($xw, $config);
            $xw->endElement();
        }
        if (isset($this->status)
            && (ValueXMLLocationEnum::PARENT_ATTRIBUTE !== $this->_valueXMLLocations[self::FIELD_STATUS]
                || $this->status->_nonValueFieldDefined())) {
            $xw->startElement(self::FIELD_STATUS);
            $this->status->xmlSerialize($xw, $config, $this->_valueXMLLocations[self::FIELD_STATUS]);
            $xw->endElement();
        }
        if (isset($this->orderedItem)) {
            $xw->startElement(self::FIELD_ORDERED_ITEM);
            $this->orderedItem->xmlSerialize($xw, $config);
            $xw->endElement();
        }
        if (isset($this->patient)) {
            $xw->startElement(self::FIELD_PATIENT);
            $this->patient->xmlSerialize($xw, $config);
            $xw->endElement();
        }
        if (isset($this->dispense)) {
            foreach ($this->dispense as $v) {
                $xw->startElement(self::FIELD_DISPENSE);
                $v->xmlSerialize($xw, $config);
                $xw->endElement();
            }
        }
        if ($rootOpened ?? false) {
            $xw->endElement();
        }
        if ($docStarted ?? false) {
            $xw->endDocument();
        }
        return $xw;
    }

    /**
     * @param string|\stdClass $decoded
     * @param null|\DCarbone\PHPFHIRGenerated\Encoding\UnserializeConfig $config
     * @param null|\DCarbone\PHPFHIRGenerated\Versions\DSTU1\Types\FHIRResource\FHIRSupply $type
     * @return \DCarbone\PHPFHIRGenerated\Versions\DSTU1\Types\FHIRResource\FHIRSupply
     * @throws \Exception
     */
    public static function jsonUnserialize(string|\stdClass $decoded,
                                           null|UnserializeConfig $config = null,
                                           null|ResourceTypeInterface $type = null): self
    {
        if (null === $type) {
            $type = new static();
        } else if (!($type instanceof FHIRSupply)) {
            throw new \RuntimeException(sprintf(
                '%s::jsonUnserialize - $type must be instance of \\%s or null, %s seen.',
                ltrim(substr(__CLASS__, (int)strrpos(__CLASS__, '\\')), '\\'),
                static::class,
                get_class($type)
            ));
        }
        if (null === $config) {
            $config = (new Version())->getConfig()->getUnserializeConfig();
        }
        if (is_string($decoded)) {
            $decoded = json_decode(json: $decoded,
                                associative: false,
                                depth: $config->getJSONDecodeMaxDepth(),
                                flags: $config->getJSONDecodeOpts());
        }
        parent::jsonUnserialize($decoded, $config, $type); 
        if (isset($decoded->kind) || property_exists($decoded, self::FIELD_KIND)) {
            if (is_array($decoded->kind)) {
                $type->setKind(FHIRCodeableConcept::jsonUnserialize(reset($decoded->kind), $config));
            } else {
                $type->setKind(FHIRCodeableConcept::jsonUnserialize($decoded->kind, $config));
            }
        }
        if (isset($decoded->identifier) || property_exists($decoded, self::FIELD_IDENTIFIER)) {
            if (is_array($decoded->identifier)) {
                $type->setIdentifier(FHIRIdentifier::jsonUnserialize(reset($decoded->identifier), $config));
            } else {
                $type->setIdentifier(FHIRIdentifier::jsonUnserialize($decoded->identifier, $config));
            }
        }
        if (isset($decoded->status)
            || isset($decoded->_status)
            || property_exists($decoded, self::FIELD_STATUS)
            || property_exists($decoded, self::FIELD_STATUS_EXT)) {
            $v = $decoded->_status ?? new \stdClass();
            $v->value = $decoded->status ?? null;
            $type->setStatus(FHIRSupplyStatus::jsonUnserialize($v, $config));
        }
        if (isset($decoded->orderedItem) || property_exists($decoded, self::FIELD_ORDERED_ITEM)) {
            if (is_array($decoded->orderedItem)) {
                $type->setOrderedItem(FHIRResourceReference::jsonUnserialize(reset($decoded->orderedItem), $config));
            } else {
                $type->setOrderedItem(FHIRResourceReference::jsonUnserialize($decoded->orderedItem, $config));
            }
        }
        if (isset($decoded->patient) || property_exists($decoded, self::FIELD_PATIENT)) {
            if (is_array($decoded->patient)) {
                $type->setPatient(FHIRResourceReference::jsonUnserialize(reset($decoded->patient), $config));
            } else {
                $type->setPatient(FHIRResourceReference::jsonUnserialize($decoded->patient, $config));
            }
        }
        if (isset($decoded->dispense) || property_exists($decoded, self::FIELD_DISPENSE)) {
            if (is_object($decoded->dispense)) {
                $vals = [$decoded->dispense];
                $type->_setJSONFieldElideSingletonArray(self::FIELD_DISPENSE, true);
            } else {
                $vals = $decoded->dispense;
            }
            foreach($vals as $v) {
                $type->addDispense(FHIRSupplyDispense::jsonUnserialize($v, $config));
            }
        }
        return $type;
    }

    /**
     * @return \stdClass
     */
    public function jsonSerialize(): mixed
    {
        $out = parent::jsonSerialize();
        if (isset($this->kind)) {
            $out->kind = $this->kind;
        }
        if (isset($this->identifier)) {
            $out->identifier = $this->identifier;
        }
        if (isset($this->status)) {
            if (null !== ($val = $this->status->getValue())) {
                $out->status = $val;
            }
            if ($this->status->_nonValueFieldDefined()) {
                $ext = $this->status->jsonSerialize();
                unset($ext->value);
                $out->_status = $ext;
            }
        }
        if (isset($this->orderedItem)) {
            $out->orderedItem = $this->orderedItem;
        }
        if (isset($this->patient)) {
            $out->patient = $this->patient;
        }
        if (isset($this->dispense) && [] !== $this->dispense) {
            if ($this->_getJSONFieldElideSingletonArray(self::FIELD_DISPENSE) && 1 === count($this->dispense)) {
                $out->dispense = $this->dispense[0];
            } else {
                $out->dispense = $this->dispense;
            }
        }
        $out->resourceType = $this->_getResourceType();
        return $out;
    }
