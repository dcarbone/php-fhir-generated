        if (isset($json[self::FIELD_USE]) || array_key_exists(self::FIELD_USE, $json)) {
            $type->setUse(FHIRCoding::jsonUnserialize(
                json: $json[self::FIELD_USE],
                config: $config,
            ));
        }
        if (isset($json[self::FIELD_VALUE]) || isset($json[self::FIELD_VALUE_EXT]) || array_key_exists(self::FIELD_VALUE, $json) || array_key_exists(self::FIELD_VALUE_EXT, $json)) {
            $value = $json[self::FIELD_VALUE] ?? null;
            $ext = (array)($json[self::FIELD_VALUE_EXT] ?? []);
            $type->setValue(FHIRString::jsonUnserialize(
                json: [FHIRString::FIELD_VALUE => $value] + $ext,
                config: $config,
            ));
        }
        return $type;
    }

    /**
     * @return \stdClass
     */
    public function jsonSerialize(): mixed
    {
        $out = parent::jsonSerialize();
        if (isset($this->language)) {
            if (null !== ($val = $this->language->getValue())) {
                $out->language = $val;
            }
            $ext = $this->language->jsonSerialize();
            unset($ext->value);
            if (count((array)$ext) > 0) {
                $out->_language = $ext;
            }
        }
        if (isset($this->use)) {
            $out->use = $this->use;
        }
        if (isset($this->value)) {
            if (null !== ($val = $this->value->getValue())) {
                $out->value = $val;
            }
            $ext = $this->value->jsonSerialize();
            unset($ext->value);
            if (count((array)$ext) > 0) {
                $out->_value = $ext;
            }
        }
        return $out;
    }

    /**
     * @return string
     */
    public function __toString(): string
    {
        return self::FHIR_TYPE_NAME;
    }
