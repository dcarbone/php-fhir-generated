    public static function jsonUnserialize(string|array|\stdClass $json,
                                           null|TypeInterface $type = null,
                                           null|UnserializeConfig $config = null): self
    {
        if (null === $type) {
            $type = new static();
        } else if (!($type instanceof FHIRValueSetFilter)) {
            throw new \RuntimeException(sprintf(
                '%s::jsonUnserialize - $type must be instance of \\%s or null, %s seen.',
                ltrim(substr(__CLASS__, (int)strrpos(__CLASS__, '\\')), '\\'),
                static::class,
                get_class($type)
            ));
        }
        if (null === $config) {
            $config = (new Version())->getConfig()->getUnserializeConfig();
        }
        if (is_string($json)) {
            $json = json_decode(json: $json, associative: true, depth: $config->getJSONDecodeMaxDepth());
        } else if (is_object($json)) {
            $json = (array)$json;
        }
        parent::jsonUnserialize($json, $type, $config);
        if (isset($json[self::FIELD_PROPERTY]) || isset($json[self::FIELD_PROPERTY_EXT]) || array_key_exists(self::FIELD_PROPERTY, $json) || array_key_exists(self::FIELD_PROPERTY_EXT, $json)) {
            $value = $json[self::FIELD_PROPERTY] ?? null;
            $ext = (isset($json[self::FIELD_PROPERTY_EXT]) && is_array($json[self::FIELD_PROPERTY_EXT])) ? $json[self::FIELD_PROPERTY_EXT] : [];
            if (null !== $value) {
                if ($value instanceof FHIRCode) {
                    $type->setProperty($value);
                } else if (is_array($value)) {
                    $type->setProperty(new FHIRCode(array_merge($ext, $value)));
                } else {
                    $type->setProperty(new FHIRCode([FHIRCode::FIELD_VALUE => $value] + $ext));
                }
            } elseif ([] !== $ext) {
                $type->setProperty(new FHIRCode($ext));
            } else {
                $type->setProperty(new FHIRCode(null));
            }
        }
        if (isset($json[self::FIELD_OP]) || isset($json[self::FIELD_OP_EXT]) || array_key_exists(self::FIELD_OP, $json) || array_key_exists(self::FIELD_OP_EXT, $json)) {
            $value = $json[self::FIELD_OP] ?? null;
            $ext = (isset($json[self::FIELD_OP_EXT]) && is_array($json[self::FIELD_OP_EXT])) ? $json[self::FIELD_OP_EXT] : [];
            if (null !== $value) {
                if ($value instanceof FHIRFilterOperator) {
                    $type->setOp($value);
                } else if (is_array($value)) {
                    $type->setOp(new FHIRFilterOperator(array_merge($ext, $value)));
                } else {
                    $type->setOp(new FHIRFilterOperator([FHIRFilterOperator::FIELD_VALUE => $value] + $ext));
                }
            } elseif ([] !== $ext) {
                $type->setOp(new FHIRFilterOperator($ext));
            } else {
                $type->setOp(new FHIRFilterOperator(null));
            }
        }
        if (isset($json[self::FIELD_VALUE]) || isset($json[self::FIELD_VALUE_EXT]) || array_key_exists(self::FIELD_VALUE, $json) || array_key_exists(self::FIELD_VALUE_EXT, $json)) {
            $value = $json[self::FIELD_VALUE] ?? null;
            $ext = (isset($json[self::FIELD_VALUE_EXT]) && is_array($json[self::FIELD_VALUE_EXT])) ? $json[self::FIELD_VALUE_EXT] : [];
            if (null !== $value) {
                if ($value instanceof FHIRString) {
                    $type->setValue($value);
                } else if (is_array($value)) {
                    $type->setValue(new FHIRString(array_merge($ext, $value)));
                } else {
                    $type->setValue(new FHIRString([FHIRString::FIELD_VALUE => $value] + $ext));
                }
            } elseif ([] !== $ext) {
                $type->setValue(new FHIRString($ext));
            } else {
                $type->setValue(new FHIRString(null));
            }
        }
        return $type;
    }

    /**
     * @return \stdClass
     */
    public function jsonSerialize(): mixed
    {
        $out = parent::jsonSerialize();
        if (isset($this->property)) {
            if (null !== ($val = $this->property->getValue())) {
                $out->property = $val;
            }
            $ext = $this->property->jsonSerialize();
            unset($ext->value);
            if (count((array)$ext) > 0) {
                $out->_property = $ext;
            }
        }
        if (isset($this->op)) {
            if (null !== ($val = $this->op->getValue())) {
                $out->op = $val;
            }
            $ext = $this->op->jsonSerialize();
            unset($ext->value);
            if (count((array)$ext) > 0) {
                $out->_op = $ext;
            }
        }
        if (isset($this->value)) {
            if (null !== ($val = $this->value->getValue())) {
                $out->value = $val;
            }
            $ext = $this->value->jsonSerialize();
            unset($ext->value);
            if (count((array)$ext) > 0) {
                $out->_value = $ext;
            }
        }
        return $out;
    }

    /**
     * @return string
     */
    public function __toString(): string
    {
        return self::FHIR_TYPE_NAME;
    }
