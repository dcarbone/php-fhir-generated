            $xw->endElement();
        }
        if (isset($this->indicationCodeableConcept)) {
            $xw->startElement(self::FIELD_INDICATION_CODEABLE_CONCEPT);
            $this->indicationCodeableConcept->xmlSerialize($xw, $config);
            $xw->endElement();
