                ltrim(substr(__CLASS__, (int)strrpos(__CLASS__, '\\')), '\\'),
                static::class,
                get_class($type)
            ));
        }
        if (null === $config) {
            $config = (new Version())->getConfig()->getUnserializeConfig();
        }
        if (is_string($json)) {
            $json = json_decode(json: $json, associative: true, depth: $config->getJSONDecodeMaxDepth());
        } else if (is_object($json)) {
            $json = (array)$json;
        }
        parent::jsonUnserialize($json, $type, $config);
        if (isset($json[self::FIELD_TYPE]) || array_key_exists(self::FIELD_TYPE, $json)) {
            if ($json[self::FIELD_TYPE] instanceof FHIRCodeableConcept) {
                $type->setType($json[self::FIELD_TYPE]);
            } else {
                $type->setType(new FHIRCodeableConcept($json[self::FIELD_TYPE]));
            }
        }
        if (isset($json[self::FIELD_VALUE_QUANTITY]) || array_key_exists(self::FIELD_VALUE_QUANTITY, $json)) {
            if ($json[self::FIELD_VALUE_QUANTITY] instanceof FHIRQuantity) {
                $type->setValueQuantity($json[self::FIELD_VALUE_QUANTITY]);
            } else {
                $type->setValueQuantity(new FHIRQuantity($json[self::FIELD_VALUE_QUANTITY]));
            }
        }
        if (isset($json[self::FIELD_VALUE_MONEY]) || array_key_exists(self::FIELD_VALUE_MONEY, $json)) {
            if ($json[self::FIELD_VALUE_MONEY] instanceof FHIRMoney) {
                $type->setValueMoney($json[self::FIELD_VALUE_MONEY]);
            } else {
                $type->setValueMoney(new FHIRMoney($json[self::FIELD_VALUE_MONEY]));
            }
        }
        if (isset($json[self::FIELD_EXCEPTION]) || array_key_exists(self::FIELD_EXCEPTION, $json)) {
            if (is_array($json[self::FIELD_EXCEPTION])) {
                foreach($json[self::FIELD_EXCEPTION] as $v) {
                    if ($v instanceof FHIRCoverageException) {
                        $type->addException($v);
                    } else {
                        $type->addException(new FHIRCoverageException($v));
                    }
                }
            } elseif ($json[self::FIELD_EXCEPTION] instanceof FHIRCoverageException) {
                $type->addException($json[self::FIELD_EXCEPTION]);
            } else {
                $type->addException(new FHIRCoverageException($json[self::FIELD_EXCEPTION]));
            }
        }
        return $type;
    }

    /**
     * @return \stdClass
     */
    public function jsonSerialize(): mixed
    {
        $out = parent::jsonSerialize();
        if (isset($this->type)) {
            $out->type = $this->type;
        }
        if (isset($this->valueQuantity)) {
            $out->valueQuantity = $this->valueQuantity;
        }
        if (isset($this->valueMoney)) {
            $out->valueMoney = $this->valueMoney;
        }
        if (isset($this->exception) && [] !== $this->exception) {
            $out->exception = $this->exception;
        }
        return $out;
    }

    /**
     * @return string
     */
    public function __toString(): string
    {
        return self::FHIR_TYPE_NAME;
    }
