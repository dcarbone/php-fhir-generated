            $v = $json->_criteria ?? new \stdClass();
            $v->value = $json->criteria ?? null;
            $type->setCriteria(FHIRString::jsonUnserialize($v, $config));
        }
        return $type;
    }

    /**
     * @return \stdClass
     */
    public function jsonSerialize(): mixed
    {
        $out = parent::jsonSerialize();
        if (isset($this->identifier)) {
            $out->identifier = $this->identifier;
        }
        if (isset($this->code)) {
            $out->code = $this->code;
        }
        if (isset($this->name)) {
            if (null !== ($val = $this->name->getValue())) {
                $out->name = $val;
            }
            if ($this->name->_nonValueFieldDefined()) {
                $ext = $this->name->jsonSerialize();
                unset($ext->value);
                $out->_name = $ext;
            }
        }
        if (isset($this->description)) {
            if (null !== ($val = $this->description->getValue())) {
                $out->description = $val;
            }
            if ($this->description->_nonValueFieldDefined()) {
                $ext = $this->description->jsonSerialize();
                unset($ext->value);
                $out->_description = $ext;
            }
        }
        if (isset($this->criteria)) {
            if (null !== ($val = $this->criteria->getValue())) {
                $out->criteria = $val;
            }
            if ($this->criteria->_nonValueFieldDefined()) {
                $ext = $this->criteria->jsonSerialize();
                unset($ext->value);
                $out->_criteria = $ext;
            }
        }
        return $out;
    }
