                $type->setCode(new FHIRResourceType(null));
            }
        }
        if (isset($json[self::FIELD_PARAM]) || isset($json[self::FIELD_PARAM_EXT]) || array_key_exists(self::FIELD_PARAM, $json) || array_key_exists(self::FIELD_PARAM_EXT, $json)) {
            $value = $json[self::FIELD_PARAM] ?? null;
            $ext = (isset($json[self::FIELD_PARAM_EXT]) && is_array($json[self::FIELD_PARAM_EXT])) ? $json[self::FIELD_PARAM_EXT] : [];
            if (null !== $value) {
                if ($value instanceof FHIRString) {
                    $type->addParam($value);
                } else if (is_array($value)) {
                    foreach($value as $i => $v) {
                        if ($v instanceof FHIRString) {
                            $type->addParam($v);
                        } else {
                            $iext = (isset($ext[$i]) && is_array($ext[$i])) ? $ext[$i] : [];
                            if (is_array($v)) {
                                $type->addParam(new FHIRString(array_merge($v, $iext)));
                            } else {
                                $type->addParam(new FHIRString([FHIRString::FIELD_VALUE => $v] + $iext));
                            }
                        }
                    }
                } elseif (is_array($value)) {
                    $type->addParam(new FHIRString(array_merge($ext, $value)));
                } else {
                    $type->addParam(new FHIRString([FHIRString::FIELD_VALUE => $value] + $ext));
                }
            } elseif ([] !== $ext) {
                foreach($ext as $iext) {
                    $type->addParam(new FHIRString($iext));
                }
            } else {
                $type->addParam(new FHIRString(null));
            }
        }
        if (isset($json[self::FIELD_DOCUMENTATION]) || isset($json[self::FIELD_DOCUMENTATION_EXT]) || array_key_exists(self::FIELD_DOCUMENTATION, $json) || array_key_exists(self::FIELD_DOCUMENTATION_EXT, $json)) {
            $value = $json[self::FIELD_DOCUMENTATION] ?? null;
            $ext = (isset($json[self::FIELD_DOCUMENTATION_EXT]) && is_array($json[self::FIELD_DOCUMENTATION_EXT])) ? $json[self::FIELD_DOCUMENTATION_EXT] : [];
            if (null !== $value) {
                if ($value instanceof FHIRString) {
                    $type->setDocumentation($value);
                } else if (is_array($value)) {
                    $type->setDocumentation(new FHIRString(array_merge($ext, $value)));
                } else {
                    $type->setDocumentation(new FHIRString([FHIRString::FIELD_VALUE => $value] + $ext));
                }
            } elseif ([] !== $ext) {
                $type->setDocumentation(new FHIRString($ext));
            } else {
                $type->setDocumentation(new FHIRString(null));
            }
        }
        return $type;
    }

    /**
     * @return \stdClass
     */
    public function jsonSerialize(): mixed
    {
        $out = parent::jsonSerialize();
        if (isset($this->code)) {
            if (null !== ($val = $this->code->getValue())) {
                $out->code = $val;
            }
            $ext = $this->code->jsonSerialize();
            unset($ext->value);
            if (count((array)$ext) > 0) {
                $out->_code = $ext;
            }
        }
        if (isset($this->param) && [] !== $this->param) {
            $vals = [];
            $exts = [];
            foreach ($this->param as $v) {
                $val = $v->getValue();
                $ext = $v->jsonSerialize();
                unset($ext->value);
                if (null !== $val) {
                    $vals[] = $val;
                }
                if ([] !== $ext) {
                    $exts[] = $ext;
                }
            }
            if ([] !== $vals) {
                $out->param = $vals;
            }
            if (count((array)$ext) > 0) {
                $out->_param = $exts;
            }
        }
        if (isset($this->documentation)) {
            if (null !== ($val = $this->documentation->getValue())) {
                $out->documentation = $val;
            }
            $ext = $this->documentation->jsonSerialize();
            unset($ext->value);
            if (count((array)$ext) > 0) {
                $out->_documentation = $ext;
            }
        }
        return $out;
    }

