            if ($this->_getJSONFieldElideSingletonArray(self::FIELD_ELEMENT) && 1 === count($this->element)) {
                $out->element = $this->element[0];
            } else {
                $out->element = $this->element;
            }
        }
        $out->resourceType = $this->_getResourceType();
        return $out;
    }
