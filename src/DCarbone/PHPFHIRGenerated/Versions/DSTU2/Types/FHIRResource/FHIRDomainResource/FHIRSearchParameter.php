                $out->target = $vals;
            }
            if (count((array)$ext) > 0) {
                $out->_target = $exts;
            }
        }
        $out->resourceType = $this->_getResourceType();
        return $out;
    }

    /**
     * @return string
     */
    public function __toString(): string
    {
        return self::FHIR_TYPE_NAME;
    }
