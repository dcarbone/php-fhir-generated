                    $type->setReleaseDate(new FHIRDateTime([FHIRDateTime::FIELD_VALUE => $value] + $ext));
                }
            } elseif ([] !== $ext) {
                $type->setReleaseDate(new FHIRDateTime($ext));
            } else {
                $type->setReleaseDate(new FHIRDateTime(null));
            }
        }
        return $type;
    }

