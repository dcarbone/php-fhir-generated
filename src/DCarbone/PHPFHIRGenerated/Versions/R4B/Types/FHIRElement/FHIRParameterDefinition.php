            $value = $json[self::FIELD_PROFILE] ?? null;
            $type->setProfile(FHIRCanonical::jsonUnserialize(
                (is_array($value) ? $value : [FHIRCanonical::FIELD_VALUE => $value]) + ($json[self::FIELD_PROFILE_EXT] ?? []),
                $config,
            ));
        }
        return $type;
    }

