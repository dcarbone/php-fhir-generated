        if (isset($json[self::FIELD_CRITERIA]) || isset($json[self::FIELD_CRITERIA_EXT]) || array_key_exists(self::FIELD_CRITERIA, $json) || array_key_exists(self::FIELD_CRITERIA_EXT, $json)) {
