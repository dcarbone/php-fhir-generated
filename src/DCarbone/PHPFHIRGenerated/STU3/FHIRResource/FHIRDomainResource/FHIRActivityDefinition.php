            $out->{self::FIELD_TOPIC} = [];
            foreach($vs as $v) {
                $out->{self::FIELD_TOPIC}[] = $v;
            }
        }
        if ([] !== ($vs = $this->getContributor())) {
            $out->{self::FIELD_CONTRIBUTOR} = [];
            foreach($vs as $v) {
                $out->{self::FIELD_CONTRIBUTOR}[] = $v;
            }
        }
        if ([] !== ($vs = $this->getContact())) {
            $out->{self::FIELD_CONTACT} = [];
            foreach($vs as $v) {
                $out->{self::FIELD_CONTACT}[] = $v;
            }
        }
        if (null !== ($v = $this->getCopyright())) {
            if (null !== ($val = $v->getValue())) {
                $out->{self::FIELD_COPYRIGHT} = $val;
            }
            $ext = $v->jsonSerialize();
            unset($ext->{FHIRMarkdown::FIELD_VALUE});
            if (count((array)$ext) > 0) {
                $out->{self::FIELD_COPYRIGHT_EXT} = $ext;
            }
        }
        if ([] !== ($vs = $this->getRelatedArtifact())) {
            $out->{self::FIELD_RELATED_ARTIFACT} = [];
            foreach($vs as $v) {
                $out->{self::FIELD_RELATED_ARTIFACT}[] = $v;
            }
        }
        if ([] !== ($vs = $this->getLibrary())) {
            $out->{self::FIELD_LIBRARY} = [];
            foreach($vs as $v) {
                $out->{self::FIELD_LIBRARY}[] = $v;
            }
        }
        if (null !== ($v = $this->getKind())) {
            $out->{self::FIELD_KIND} = $v;
        }
        if (null !== ($v = $this->getCode())) {
            $out->{self::FIELD_CODE} = $v;
        }
        if (null !== ($v = $this->getTimingTiming())) {
            $out->{self::FIELD_TIMING_TIMING} = $v;
        }
        if (null !== ($v = $this->getTimingDateTime())) {
            if (null !== ($val = $v->getValue())) {
                $out->{self::FIELD_TIMING_DATE_TIME} = $val;
            }
            $ext = $v->jsonSerialize();
            unset($ext->{FHIRDateTime::FIELD_VALUE});
            if (count((array)$ext) > 0) {
                $out->{self::FIELD_TIMING_DATE_TIME_EXT} = $ext;
            }
        }
        if (null !== ($v = $this->getTimingPeriod())) {
            $out->{self::FIELD_TIMING_PERIOD} = $v;
        }
        if (null !== ($v = $this->getTimingRange())) {
            $out->{self::FIELD_TIMING_RANGE} = $v;
        }
        if (null !== ($v = $this->getLocation())) {
            $out->{self::FIELD_LOCATION} = $v;
        }
        if ([] !== ($vs = $this->getParticipant())) {
            $out->{self::FIELD_PARTICIPANT} = [];
            foreach($vs as $v) {
                $out->{self::FIELD_PARTICIPANT}[] = $v;
            }
        }
        if (null !== ($v = $this->getProductReference())) {
            $out->{self::FIELD_PRODUCT_REFERENCE} = $v;
        }
        if (null !== ($v = $this->getProductCodeableConcept())) {
            $out->{self::FIELD_PRODUCT_CODEABLE_CONCEPT} = $v;
        }
        if (null !== ($v = $this->getQuantity())) {
            $out->{self::FIELD_QUANTITY} = $v;
        }
        if ([] !== ($vs = $this->getDosage())) {
            $out->{self::FIELD_DOSAGE} = [];
            foreach($vs as $v) {
                $out->{self::FIELD_DOSAGE}[] = $v;
            }
        }
        if ([] !== ($vs = $this->getBodySite())) {
            $out->{self::FIELD_BODY_SITE} = [];
            foreach($vs as $v) {
                $out->{self::FIELD_BODY_SITE}[] = $v;
            }
        }
        if (null !== ($v = $this->getTransform())) {
            $out->{self::FIELD_TRANSFORM} = $v;
        }
        if ([] !== ($vs = $this->getDynamicValue())) {
            $out->{self::FIELD_DYNAMIC_VALUE} = [];
            foreach($vs as $v) {
                $out->{self::FIELD_DYNAMIC_VALUE}[] = $v;
            }
        }

        $out->{PHPFHIRConstants::JSON_FIELD_RESOURCE_TYPE} = $this->_getResourceType();

        return $out;
    }

