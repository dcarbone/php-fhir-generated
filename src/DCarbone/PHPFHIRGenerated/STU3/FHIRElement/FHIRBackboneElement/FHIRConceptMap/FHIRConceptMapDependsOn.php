        if (([] === $locs || (isset($locs[0]) && PHPFHIRXmlLocationEnum::ELEMENT === $locs[0])) && null !== ($v = $this->getDisplay())) {
            $xw->startElement(self::FIELD_DISPLAY);
            $v->xmlSerialize($xw, $config);
