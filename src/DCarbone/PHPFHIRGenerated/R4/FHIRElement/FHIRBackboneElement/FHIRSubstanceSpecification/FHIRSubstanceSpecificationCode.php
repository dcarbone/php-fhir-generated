            } elseif (self::FIELD_STATUS_DATE === $childName) {
                $type->setStatusDate(FHIRDateTime::xmlUnserialize($n, null, $config), PHPFHIRXmlLocationEnum::ELEMENT);
            } elseif (self::FIELD_COMMENT === $childName) {
