        if (null !== ($v = $this->getCode())) {
            $xw->startElement(self::FIELD_CODE);
            $v->xmlSerialize($xw, $config);
            $xw->endElement();
        }
        if (null !== ($v = $this->getValue())) {
            $xw->startElement(self::FIELD_VALUE);
            $v->xmlSerialize($xw, $config);
            $xw->endElement();
        }
        foreach ($this->getVariable() as $v) {
            $xw->startElement(self::FIELD_VARIABLE);
            $v->xmlSerialize($xw, $config);
            $xw->endElement();
        }
        foreach ($this->getAttributeEstimate() as $v) {
            $xw->startElement(self::FIELD_ATTRIBUTE_ESTIMATE);
            $v->xmlSerialize($xw, $config);
            $xw->endElement();
        }
        if (isset($openedRoot) && $openedRoot) {
            $xw->endElement();
        }
        if (isset($docStarted) && $docStarted) {
            $xw->endDocument();
        }
        return $xw;
    }

    /**
     * @return \stdClass
     */
    public function jsonSerialize(): mixed
    {
        $out = parent::jsonSerialize();
        if (null !== ($v = $this->getCode())) {
            $out->{self::FIELD_CODE} = $v;
        }
        if (null !== ($v = $this->getValue())) {
            $out->{self::FIELD_VALUE} = $v;
        }
        if ([] !== ($vs = $this->getVariable())) {
            $out->{self::FIELD_VARIABLE} = [];
            foreach($vs as $v) {
                $out->{self::FIELD_VARIABLE}[] = $v;
            }
        }
        if ([] !== ($vs = $this->getAttributeEstimate())) {
            $out->{self::FIELD_ATTRIBUTE_ESTIMATE} = [];
            foreach($vs as $v) {
                $out->{self::FIELD_ATTRIBUTE_ESTIMATE}[] = $v;
            }
        }

        return $out;
    }

    /**
     * @return string
     */
    public function __toString(): string
    {
        return self::FHIR_TYPE_NAME;
    }
