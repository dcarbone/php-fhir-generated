            $out->{self::FIELD_DISPENSE} = [];
            foreach($vs as $v) {
                $out->{self::FIELD_DISPENSE}[] = $v;
            }
        }

        $out->{PHPFHIRConstants::JSON_FIELD_RESOURCE_TYPE} = $this->_getResourceType();

        return $out;
    }

